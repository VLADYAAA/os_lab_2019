#!/bin/bash

# Скрипт для подсчета количества и среднего арифметического аргументов

if [ $# -eq 0 ]; then
    echo "Ошибка: Необходимо передать числа как аргументы"
    echo "Использование: $0 <число1> <число2> ... <числоN>"
    exit 1
fi

count=$#
sum=0

# Суммируем все аргументы
for num in "$@"; do
    # Проверяем, что аргумент - число
    if ! [[ "$num" =~ ^-?[0-9]+$ ]]; then
        echo "Ошибка: '$num' не является числом" >&2
        exit 1
    fi
    sum=$((sum + num))
done

# Вычисляем среднее арифметическое (без bc)
average=$((sum / count))
remainder=$((sum % count))

# Выводим результаты
echo "Количество чисел: $count"
echo "Сумма чисел: $sum"
echo "Среднее арифметическое: $average "